l=[10,20,30,40,50,60,70,80,90]
print("Original List =",l)

#insert()

l.insert(9,100) #use to insert data along with its index number and value at given position
print("After Inserting data  List =",l)

#append()
l.append(110) #use to append data in list at very last location 
print("After Inserting data  List =",l)

n=[10,20,30]
l.append(n) #append data with its datatype
print("After Inserting data  List =",l)

#exdend()
n=[10,20,30]
l.extend(n) #extend list with value passed as an argument only append value not a full datatype
print("After Inserting data  List =",l)

