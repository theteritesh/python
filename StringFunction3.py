# Gives Boolean Result

# isalpha()
s="MynameRitesh"
print(s.isalpha())

s="Mynameis123Ritesh"
print(s.isalpha())

# isdigit()

r="123456789"
print(r.isdigit())

r="MynameRitesh"
print(r.isdigit())

# isalnum()

w="Mynameis123Ritesh"
print(w.isalnum())

w="12345"
print(w.isalnum())

w="Ritesh"
print(w.isalnum())

w="Rit@esh"
print(w.isalnum())